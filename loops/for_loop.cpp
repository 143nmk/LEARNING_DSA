/* Cpp code to demonstarte the use of for loop 

  input: No input is required
  output: Prints numbers from 1 to 10 using for loop

*/

#include <iostream>
using namespace std;

int main()
{
    // for loop to print numbers from 1 to 10
    for (int i = 1; i <= 10; i++)
    {
        cout << i << " ";
    }
    cout << endl; // for a new line after the loop
    return 0;
}